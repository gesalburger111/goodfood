package food2;


public class UserBean {
    String name;
    String place;
    int idx;
    int score;
    int count;
    
    
    public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int socre) {
		this.score = socre;
	}
    public String getname(){
        return name;
    }
    public void setname(String name) {
        this.name = name;
    }
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	
	
 
}